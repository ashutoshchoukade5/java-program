package com.operator.demo;

public class ShiftOp {

	public static void main(String[] args) {
    //left shift operator // 10<<3= 10*2^3 
		//right shift operator // 10>>3= 10/2^3	 
		
		System.out.println(12<<3);//12*2^3
		System.out.println(12>>2);//12/2^2
		
    System.out.println(12>>3);
    System.out.println(12>>>3);
    System.out.println(-12>>>3);
		
		
		
		
	}

}
